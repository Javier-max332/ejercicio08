        function mostrarCancion(albumNombre, numCancion) {
            //se busca el álbum por su nombre
            const album = albums.find(a => a.album === albumNombre);

            //ya teniendo el álbum donde encontrarémos la canción, ahora vamos a 
            //sacar la canción que se encuentra en ese álbum por num de posición
            const cancion = album.canciones[numCancion];

            //asiganmos la URL al iframe para mostrar la canción.

            //encontrar a cual lista ordenada le dio clic.
            document.addEventListener("click", (event) => {
                if (event.target.closest("#album-revolver")) {
                    document.getElementById("videoPlayer_Revolver").src = cancion.url;
                } else if (event.target.closest("#album-magicalMysteryTour")) {
                    document.getElementById("videoPlayer_magical").src = cancion.url;
                } else if (event.target.closest("#album-yellowSubmarine")) {
                    document.getElementById("videoPlayer_Yellow").src = cancion.url;
                }
            });     
            console.log(`Reproduciendo: ${cancion.nombre} (${album.album})`);
        }
